package juegos;

public class algoRandomDeJavi {

public static void main(String[] args) {
	
	System.out.println("con for");
	
	for(int i = 1; i<= 10;i++) {
		System.out.println(i);
		}
	
	System.out.println();
	
	int i = 0;
	
	System.out.println("con while");
	
	while( i < 11) {
		System.out.println(i);
		i++;
	}
	
	System.out.println("con do while");
	
	int e = 1;
	
	do {
		System.out.println(e);
		e++;
	}while(e < 11);
	
	}
}